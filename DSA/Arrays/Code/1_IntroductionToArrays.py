# Introduction to Arrays in Python
# Arrays/Lists are collections of elements stored in contiguous memory

# List initialization
arr = [10, 20, 30, 40, 50]

# Accessing elements
print("First element:", arr[0])
print("Last element:", arr[-1])

# Iterating through list
print("All elements:", arr)

# List length
print("List length:", len(arr))

# List operations
arr.append(60)  # Add element
print("After appending 60:", arr)

arr.pop()  # Remove last element
print("After removing last element:", arr)
